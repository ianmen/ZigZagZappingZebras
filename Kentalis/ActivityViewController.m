-(void)loadActivities

{
    //First load some dummy data
    int i = 0;
    while ( i < 10) {
        
        Activity *ac = [Activity create];
        ac.title = @"Activity 1";
        [ac save];
        i++;
        
    }
    
    //Load them in array
    self.activtyArray = [Activity all];
    
}

